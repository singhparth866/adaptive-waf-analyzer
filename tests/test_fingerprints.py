# ── Orchestrator ──────────────────────────────────────────────────────────────

class TestOrchestrator:
    def setup_method(self):
        self.orch = FingerprintOrchestrator()

    def test_cloudflare_wins_with_cf_ray(self):
        r = _mock_response({"cf-ray": "8abc-LHR", "server": "cloudflare"})
        result = self.orch.identify(r)
        assert result.vendor == WAFVendor.CLOUDFLARE

    def test_unknown_returns_none(self):
        r = _mock_response({"server": "nginx", "x-custom": "foo"})
        result = self.orch.identify(r)
        assert result.vendor == WAFVendor.NONE

    def test_identify_all_returns_list(self):
        r = _mock_response({"cf-ray": "abc", "server": "cloudflare"})
        results = self.orch.identify_all(r)
        assert isinstance(results, list)
        assert len(results) >= 1
        assert results[0].vendor == WAFVendor.CLOUDFLARE

    def test_identify_all_empty_for_no_signals(self):
        r = _mock_response({"content-type": "text/html"})
        results = self.orch.identify_all(r)
        assert results == []
