from core.classifiers.orchestrator import FingerprintOrchestrator, CONFIDENCE_THRESHOLD


def _mock_response(headers: dict, status: int = 200,
                   body: str = "", cookies: dict | None = None):
    r = MagicMock()
    r.headers    = headers
    r.status_code = status
    r.cookies    = cookies or {}
    r.content    = body.encode()
    r.text       = body
    return r


# ── Cloudflare ────────────────────────────────────────────────────────────────

class TestCloudflareDetector:
    def setup_method(self):
        self.det = CloudflareDetector()

    def test_cf_ray_header_detected(self):
        r = _mock_response({"cf-ray": "8abc-LHR", "server": "cloudflare"})
        result = self.det.score(r, "")
        assert result.vendor == WAFVendor.CLOUDFLARE
        assert result.confidence >= CONFIDENCE_THRESHOLD

    def test_cf_cache_status(self):
        r = _mock_response({"cf-cache-status": "MISS"})
        result = self.det.score(r, "")
        assert result.confidence >= 0.20

    def test_block_body_pattern(self):
        body = "Attention Required! | Cloudflare  Ray ID: 7abc1234def56789"
        r = _mock_response({}, 403, body)
        result = self.det.score(r, body)
        assert result.confidence >= CONFIDENCE_THRESHOLD

    def test_no_signals_zero_confidence(self):
        r = _mock_response({"server": "nginx"})
        result = self.det.score(r, "Hello world")
        assert result.confidence == 0.0

    def test_cf_cookie(self):
        r = _mock_response({}, cookies={"cf_clearance": "abc123"})
        result = self.det.score(r, "")
        assert result.confidence >= 0.20


